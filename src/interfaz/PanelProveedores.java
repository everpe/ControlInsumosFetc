    /**
     * Evento cuando se clickea la Table del Panel
     * @param evt 
     */
    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int col=table.getColumnModel().getColumnIndexAtX(evt.getX());
        int row=evt.getY()/table.getRowHeight();
        
        //Verifica que lo que clikeo fué un boton
        if(row < table.getRowCount()  && row >= 0 && col < table.getColumnCount()
                && col >= 0){
            Object value=table.getValueAt(row, col);
            if(value instanceof JButton){
                ((JButton)value).doClick();
                JButton btn= (JButton)value;
                
                if(btn.getName().equals("btnEdit")){
                    this.editProvider();
                }else if(btn.getName().equals("btnDelete")){
                    this.deleteProvider();
                }
                System.out.println(btn.getName());
            }
//            
            
        }
    }//GEN-LAST:event_tableMouseClicked

    
    
    /**
     * Se llama al dar click en la tabla, boton editar
     * Interacrua con la BD del Restaurante
     */
    public void editProvider(){
       
        Provider provOld = this.tableController.getProviderFromTable(this.table);
        if(provOld == null)
            JOptionPane.showMessageDialog(this, "No Se Seleccionó Ningún Proveedor");
        else
        {            
            this.dialogProvider= new  DialogNewProvider(null, true,2);
            this.dialogProvider.setProvider(provOld);
            this.dialogProvider.setVisible(true);
            //el dialogo modifica el provedor de la table y me da uno nuevo
            Provider provNew=dialogProvider.getProveedor();
            
            boolean ban = this.restaurant.getBd().getCrudProvider().updateProvider(provOld, provNew);
           
            if(ban)
            {
                JOptionPane.showMessageDialog(this, "Proveedor  Actualizado Correctamente");
                this.restaurant.updateDataBase();
                //this.paintTableProveedores();
                this.createTable(this.restaurant.getProviders());
            }
            else
            {
                JOptionPane.showMessageDialog(this, "No Se Pudo Eliminar El Proveedor De La Base De Datos");
            }          
        }  
        
        System.out.println("editProvide");
    }
    
    
    
    /**
     * Se llama al darle click a la tabla en el boton delete.
     */
    public void deleteProvider(){
        Provider prov = this.tableController.getProviderFromTable(this.table);
        if(prov == null)
            JOptionPane.showMessageDialog(this, "No Se Seleccionó Ningún Proveedor");
        else
        {            
            String name = prov.getName();
            int opc = JOptionPane.showConfirmDialog(null, "Realmente Desea Eliminar El Proveedor " + name.toUpperCase(),
                    "Eliminar Proveedor", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            //0 para si || 1 para no
            if (opc == 0)
            {
                boolean ban = this.restaurant.getBd().getCrudProvider().deleteProvider(prov);
                //Si se elimino el proveedor de la base de datos.
                if(ban)
                {
                    JOptionPane.showMessageDialog(this, "Proveedor " + name.toUpperCase() + " Eliminado Correctamente");
                    this.restaurant.updateDataBase();
                    //this.paintTableProveedores();
                    this.createTable(this.restaurant.getProviders());
//                    this.pintor.paintTableProviders(this.tequilazo.getProveedores(), this.modeloListaProveedores);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "No Se Pudo Eliminar El Proveedor De La Base De Datos");
                }
            }            
        }   
        System.out.println("deleteProvide");
    }

