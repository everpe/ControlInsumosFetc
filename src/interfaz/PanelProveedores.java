    /**
     * Metodo que crea y llena la Tabla.
     * @param providers 
     */
    public void createTable(ArrayList<Provider>providers)
    {
        //inicializa estilos de la tabla y sus botones.
        this.initTable();
        //Limpia datos viejos de la tabla
        this.clearDataFromTable();
        //Llena la Tabla con Providers del Restaurante.
        if(providers.size()>0&&providers!=null){
            try{
                for(int i=0;i<providers.size();i++)
                {
                    Provider prov=providers.get(i);
                    Object [] arreglo ={prov.getId(),prov.getName(),prov.getNit()
                            ,prov.getCellphone(),prov.getEmail(),prov.getDirection(),edit,delete};
                    modelo.addRow(arreglo);    
                }
                table.setModel(modelo);
            }catch(Exception e){ 
                System.out.println("ERROR:pintando tabla proveedores"+e.getMessage());
            }
        }
       
    }
    
    
