    private void btnCrearProveedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearProveedorMouseClicked
        this.dialogProvider= new  DialogNewProvider(this, true,1);
        this.dialogProvider.setVisible(true);
        //obtengo el Provider
        Provider prov=dialogProvider.getProveedor();
        boolean bandera=false;
         
        if(prov!=null)
            bandera=this.restaurante.getBd().getCrudProvider().createProvider(prov); 
        
        if(bandera){
            this.restaurante.updateDataBase(); 
            JOptionPane.showMessageDialog(this, "Proveedor: "+prov.getName()+" Creado Correctamente"
                    + " 'recargue la lista de Proveedores' ");
        }else{
            JOptionPane.showMessageDialog(this, "NO se ha Creado un Nuevo Proveedor ");
        }
        
          
    }//GEN-LAST:event_btnCrearProveedorMouseClicked

    private void btnCreateSupplyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCreateSupplyMouseClicked
        
        ArrayList<Category>categories=this.restaurante.getBd().getCrudCategory().getAllCategories();
        System.out.println("Principal "+categories.size());
        DialogSupply dialogSupply= new DialogSupply(this, true,
                1,categories );
        dialogSupply.setVisible(true);
        
        Supply supply=dialogSupply.getSupply();
        boolean bandera=false;
//        System.out.println("El Supply creado en el Dialogo fuée:"+dialogSupply.getSupply().getName()
//                +"Su Categoria"+supply.getCategory().getName()+supply.getCategory().getUnityMeasure());
        if(supply!=null)
            bandera=this.restaurante.getBd().getCrudSupply().cretaSupply(supply); 
        
        if(bandera){
            this.restaurante.updateDataBase(); 
            JOptionPane.showMessageDialog(this, "Insumo: "+supply.getName()+" Creado Correctamente"
                    + " 'recargue la lista Insumos' ");
        }else{
            JOptionPane.showMessageDialog(this, "NO se ha Creado un Nuevo Insumo ");
        }
     
        
    }//GEN-LAST:event_btnCreateSupplyMouseClicked

