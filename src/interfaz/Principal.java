    private void btnCrearComprasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearComprasMouseClicked
        //Dialogo que permite crear una compra 
        DialogNewPurchase diaPurchase= new DialogNewPurchase(this,true,restaurante);
        diaPurchase.setVisible(true);    
        Purchase purchase= diaPurchase.getPurchase();
        
        if(purchase!=null&&purchase.getProvider()!=null&&purchase.getInsumos().size()>0){
           
           boolean bandera=this.restaurante.addPurchase(purchase);
//           boolean bandera= this.restaurante.getBd().getCrudPurchase().createPurchase(purchase);
           this.restaurante.updateDataBase();
           if(bandera){
               JOptionPane.showMessageDialog(this, "Compra Agregada Con Éxito"+purchase.getInsumos().size());
           }else{
               JOptionPane.showMessageDialog(this, "No se Pudó Agregar La Compra");
           }
        }
        
    }//GEN-LAST:event_btnCrearComprasMouseClicked

